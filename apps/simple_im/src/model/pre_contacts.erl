create(AUserId, BUserId, Message)
    when is_integer(AUserId), is_integer(BUserId) ->
    SQL = <<"SELECT create_pre_contact($1, $2, $3);">>,
    {ok, _, [{Result}]} = postgresql:exec(SQL, [AUserId, BUserId, Message]),
    {ok, Result}.




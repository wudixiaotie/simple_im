-module(simple_im_app).

-behaviour(application).

%% Application callbacks
-export([start/2, stop/1]).



%%====================================================================
%% API
%%====================================================================

start(_StartType, _StartArgs) ->
    simple_im_sup:start_link().

%%--------------------------------------------------------------------
stop(_State) ->
    case env:get(app_mode) of
        session_server ->
            dets:close(session);
        _ ->
            ok
    end.



